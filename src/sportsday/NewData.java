    void initiatelist(){
        try{
            db=new DataInputStream(new FileInputStream("Schools.txt"));
            int l=Integer.parseInt(db.readLine());
            school=new String[l];
            
            for(int i=0;i<l;i++){
                school[i]=db.readLine().trim();
            }
            school1=school;
            school2=school;
            school3=school;
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }
        
        try{
            db=new DataInputStream(new FileInputStream("Events.txt"));
            int l=Integer.parseInt(db.readLine());
            list=new String[l];
            table=new String[l];
            marks=new int[l][3];
            
            for(int i=0;i<l;i++){
                String str=db.readLine();
                
                int c=0;
                String s="",s2="";
                for(int j=0;j<str.length();j++)
                {
                    char ch=str.charAt(j);
                    
                    if(ch==','){
                        c++;
                        if(c==1){
                            list[i]=s;
                            table[i]=s2;
                        }
                        else if(c==2)
                            marks[i][0]=Integer.parseInt(s);
                        else if(c==3)
                            marks[i][1]=Integer.parseInt(s);
                        else if(c==4)
                            marks[i][2]=Integer.parseInt(s);
                        s="";
                        continue;
                    }
                    s=s+ch;
                    
                    //*****(ch!=' '&& ch!='-'&& ch!='/'&&ch!='.'&&ch!='('&&ch!=')')***//
                    
                    if((ch>='a'&&ch<='z')||(ch>='A'&&ch<='Z')||(ch>='0'&&ch<='9'))
                    s2=s2+ch;
                }
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }

        
    }
