private void ListClicked(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ListClicked
    set123();
    int n=jList1.getSelectedIndex();
    String f="",s="",t="";
    try{
    ResultSet r=st.executeQuery("SELECT * FROM DATA WHERE EVENTS='"+table[n]+"'");
    while(r.next()){
        f=r.getString("FIRST");
        s=r.getString("SECOND");
        t=r.getString("THIRD");
    }
    first.setListData(school1);
    second.setListData(school2);
    third.setListData(school3);
    
    first.setSelectedValue(f, true);
    second.setSelectedValue(s, true);
    third.setSelectedValue(t, true);
    }catch(Exception ex){JOptionPane.showMessageDialog(this, ex);}
}//GEN-LAST:event_ListClicked

